%       functions within FAST, or explore different aspects of how FAST is
%       used. Each of the scripts connects to a video within a series of
%       tutorials on how to use FAST. The tutorial series can be accessed
%       via this link: https://www.youtube.com/playlist?list=PLNbQSl1VumqhNHgNOq9oxm4_Toi4_7v3-
% 
% -------------------------------------------------------------------------
%
% (II)  Usage
%
%       All scripts can be run directly from the Tutorials Package. Most of
%       the scripts do not require any inputs. However, there are a few
%       that may require a flag/index to run. Please refer to each
%       function's documentation before running it. Also, some functions
%       will pause the script and require you to press a key in the command
%       window to continue. If you think that the script is not running (or
%       failed), check to see if the function is paused in the command
%       window.
%
%       Additionally, some functions do not run FAST at all. They only open
%       scripts that were used in the video demo.
%
% -------------------------------------------------------------------------
%
% (III) Warning
%
%       Please remember that FAST is being continuously developed and
%       upgraded! As a result, the scripts reviewed in the video tutorials
%       may not entirely match the script that is opened in this version of
%       the code. They will be similar, but some variable names/values may
%       change slightly.
%
% -------------------------------------------------------------------------
%
% end TutorialsPkg.README
